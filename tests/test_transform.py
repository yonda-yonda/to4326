def test_transform_bbox():
    assert transform.transform_bbox([382200, 2279400, 610500, 2512500], 32645) == [
        85.85296718933647,
        20.610041795245515,
        88.07596179098907,
        22.719775713801845,
    ]
    assert transform.transform_bbox([508800, 7001100, 753000, 7247400], 32660) == [
        177.17456390562774,
        63.05068519969726,
        -177.57860702499983,
        65.34932256544839,
    ]
    assert transform.transform_bbox(
        [508800, 7001100, 753000, 7247400], 32660, expand=True
    ) == [
        177.17456390562774,
        63.05068519969726,
        -177.57860702499983 + 360,
        65.34932256544839,
    ]
    assert transform.transform_bbox(
        [382200, 2279400, 10, 610500, 2512500, 50], 32645
    ) == [
        85.85296718933647,
        20.610041795245515,
        10,
        88.07596179098907,
        22.719775713801845,
        50,
    ]
    assert transform.transform_bbox(
        [508800, 7001100, 5, 753000, 7247400, 10], 32660
    ) == [
        177.17456390562774,
        63.05068519969726,
        5,
        -177.57860702499983,
        65.34932256544839,
        10,
    ]


